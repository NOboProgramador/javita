public class forEach {
public static void main(String[] args) {
    
    
    
String  [] Nombres={"luis","daniel","sergio","jose","leo","meli"};
//length es utilizado para determinar el tamaño de un arreglo ya sea vector o matriz,
//siendo ésta una variable pública que está presenta en cualquier arreglo
for(int i=0; i<Nombres.length; i++){
            System.out.println(Nombres[i]);   

    }
}
}
